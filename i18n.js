function setLanguage(lang) {
  if (!translations[lang]) lang = 'en';

  localStorage.setItem('preferredLanguage', lang);

  document.querySelectorAll('[data-i18n]').forEach(element => {
    const key = element.getAttribute('data-i18n');
    if (translations[lang][key]) {
      if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
        element.placeholder = translations[lang][key];
      } else {
        element.textContent = translations[lang][key];
      }
    }
  });

  // Update language toggle text
  const langBtn = document.querySelector('.current-lang');
  if (langBtn) {
    langBtn.innerHTML = `${translations[lang].lang_flag} ${translations[lang].lang_name}`;
  }

  // Update privacy policy links if they exist
  document.querySelectorAll('.privacy-link').forEach(link => {
    link.href = `privacy-${lang}.html`;
  });

  document.documentElement.lang = lang;
}

// Initialize
document.addEventListener('DOMContentLoaded', () => {
  const savedLang = localStorage.getItem('preferredLanguage');
  const browserLang = navigator.language.split('-')[0];
  const defaultLang = savedLang || (translations[browserLang] ? browserLang : 'en');

  setLanguage(defaultLang);
});
